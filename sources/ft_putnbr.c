#include "../includes/libft.h"

void		ft_putnbr(int n)
{
	char	c;

	if (n < 0)
	{
		if (n == -2147483648)
			write(1, "-2147483648", 11);
		else
		{
			write(1, "-", 1);
			n *= -1;
		}
	}
	c = (char)(n + 48);
	if (n < 10 && n >= 0)
		write(1, &c, 1);
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
